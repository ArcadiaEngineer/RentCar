package Entities.Abstract;

/**
 *
 * @author Lian La-Fey
 */
public abstract class Person {
    
    protected String fullName;    

    public Person(String fullName) {
        this.fullName = fullName;
    }

    public String getFullName() {
        return fullName;
    }

    @Override
    public String toString() {
        return fullName;
    }
    
    
    
}
